let num10 = 10;

if (num10 > 0 && num10 % 2 == 0) {
  console.log(`The number ${num10} is even`);
} else if (num10 > 0 && num10 % 2 == 1) {
  console.log(`The number ${num10} is odd`);
} else {
  console.log(`Error`);
}

